document.addEventListener("DOMContentLoaded", () => {
  const encryptForm = document.getElementById("encryptForm")
  const resultCard = document.getElementById("resultCard")
  const outputText = document.getElementById("outputText")
  const copyButton = document.getElementById("copyButton")
  const generateKeyButton = document.getElementById("generateKey")
  const toggleKeyButton = document.getElementById("toggleKey")
  const secretKeyInput = document.getElementById("secretKey")

  // Toggle password visibility
  toggleKeyButton.addEventListener("click", () => {
    if (secretKeyInput.type === "password") {
      secretKeyInput.type = "text"
      toggleKeyButton.innerHTML = `
                <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-eye-slash" viewBox="0 0 16 16">
                    <path d="M13.359 11.238C15.06 9.72 16 8 16 8s-3-5.5-8-5.5a7.028 7.028 0 0 0-2.79.588l.77.771A5.944 5.944 0 0 1 8 3.5c2.12 0 3.879 1.168 5.168 2.457A13.134 13.134 0 0 1 14.828 8c-.058.087-.122.183-.195.288-.335.48-.83 1.12-1.465 1.755-.165.165-.337.328-.517.486l.708.709z"/>
                    <path d="M11.297 9.176a3.5 3.5 0 0 0-4.474-4.474l.823.823a2.5 2.5 0 0 1 2.829 2.829l.822.822zm-2.943 1.299.822.822a3.5 3.5 0 0 1-4.474-4.474l.823.823a2.5 2.5 0 0 0 2.829 2.829z"/>
                    <path d="M3.35 5.47c-.18.16-.353.322-.518.487A13.134 13.134 0 0 0 1.172 8l.195.288c.335.48.83 1.12 1.465 1.755C4.121 11.332 5.881 12.5 8 12.5c.716 0 1.39-.133 2.02-.36l.77.772A7.029 7.029 0 0 1 8 13.5C3 13.5 0 8 0 8s.939-1.721 2.641-3.238l.708.709zm10.296 8.884-12-12 .708-.708 12 12-.708.708z"/>
                </svg>
            `
    } else {
      secretKeyInput.type = "password"
      toggleKeyButton.innerHTML = `
                <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-eye" viewBox="0 0 16 16">
                    <path d="M16 8s-3-5.5-8-5.5S0 8 0 8s3 5.5 8 5.5S16 8 16 8zM1.173 8a13.133 13.133 0 0 1 1.66-2.043C4.12 4.668 5.88 3.5 8 3.5c2.12 0 3.879 1.168 5.168 2.457A13.133 13.133 0 0 1 14.828 8c-.058.087-.122.183-.195.288-.335.48-.83 1.12-1.465 1.755C11.879 11.332 10.119 12.5 8 12.5c-2.12 0-3.879-1.168-5.168-2.457A13.134 13.134 0 0 1 1.172 8z"/>
                    <path d="M8 5.5a2.5 2.5 0 1 0 0 5 2.5 2.5 0 0 0 0-5zM4.5 8a3.5 3.5 0 1 1 7 0 3.5 3.5 0 0 1-7 0z"/>
                </svg>
            `
    }
  })

  // Generate random key
  generateKeyButton.addEventListener("click", () => {
    const length = 16
    const charset = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789!@#$%^&*()_-+=<>?"
    let result = ""
    for (let i = 0; i < length; i++) {
      result += charset.charAt(Math.floor(Math.random() * charset.length))
    }
    secretKeyInput.value = result
    secretKeyInput.type = "text"
    toggleKeyButton.innerHTML = `
            <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-eye-slash" viewBox="0 0 16 16">
                <path d="M13.359 11.238C15.06 9.72 16 8 16 8s-3-5.5-8-5.5a7.028 7.028 0 0 0-2.79.588l.77.771A5.944 5.944 0 0 1 8 3.5c2.12 0 3.879 1.168 5.168 2.457A13.134 13.134 0 0 1 14.828 8c-.058.087-.122.183-.195.288-.335.48-.83 1.12-1.465 1.755-.165.165-.337.328-.517.486l.708.709z"/>
                <path d="M11.297 9.176a3.5 3.5 0 0 0-4.474-4.474l.823.823a2.5 2.5 0 0 1 2.829 2.829l.822.822zm-2.943 1.299.822.822a3.5 3.5 0 0 1-4.474-4.474l.823.823a2.5 2.5 0 0 0 2.829 2.829z"/>
                <path d="M3.35 5.47c-.18.16-.353.322-.518.487A13.134 13.134 0 0 0 1.172 8l.195.288c.335.48.83 1.12 1.465 1.755C4.121 11.332 5.881 12.5 8 12.5c.716 0 1.39-.133 2.02-.36l.77.772A7.029 7.029 0 0 1 8 13.5C3 13.5 0 8 0 8s.939-1.721 2.641-3.238l.708.709zm10.296 8.884-12-12 .708-.708 12 12-.708.708z"/>
            </svg>
        `
  })

  // Copy to clipboard functionality
  copyButton.addEventListener("click", () => {
    outputText.select()
    document.execCommand("copy")

    // Show feedback
    const originalText = copyButton.innerHTML
    copyButton.innerHTML = `
            <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-check-circle me-1" viewBox="0 0 16 16">
                <path d="M8 15A7 7 0 1 1 8 1a7 7 0 0 1 0 14zm0 1A8 8 0 1 0 8 0a8 8 0 0 0 0 16z"/>
                <path d="M10.97 4.97a.235.235 0 0 0-.02.022L7.477 9.417 5.384 7.323a.75.75 0 0 0-1.06 1.06L6.97 11.03a.75.75 0 0 0 1.079-.02l3.992-4.99a.75.75 0 0 0-1.071-1.05z"/>
            </svg>
            Copied!
        `

    setTimeout(() => {
      copyButton.innerHTML = originalText
    }, 2000)
  })

  // Form submission for encryption
  encryptForm.addEventListener("submit", (e) => {
    e.preventDefault()

    const algorithm = document.getElementById("algorithm").value
    const secretKey = document.getElementById("secretKey").value
    const inputText = document.getElementById("inputText").value

    if (!secretKey || !inputText) {
      alert("Please fill in all fields")
      return
    }

    let encryptedText

    try {
      // Perform encryption based on selected algorithm
      switch (algorithm) {
        case "aes":
          encryptedText = CryptoJS.AES.encrypt(inputText, secretKey).toString()
          break
        case "des":
          encryptedText = CryptoJS.TripleDES.encrypt(inputText, secretKey).toString()
          break
        case "rc4":
          encryptedText = CryptoJS.RC4.encrypt(inputText, secretKey).toString()
          break
        case "rabbit":
          encryptedText = CryptoJS.Rabbit.encrypt(inputText, secretKey).toString()
          break
        default:
          encryptedText = CryptoJS.AES.encrypt(inputText, secretKey).toString()
      }

      // Display the result
      outputText.value = encryptedText
      resultCard.style.display = "block"
      resultCard.scrollIntoView({ behavior: "smooth" })
    } catch (error) {
      alert("Encryption failed: " + error.message)
    }
  })
})
